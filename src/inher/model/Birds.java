package inher.model;

public class Birds 
{
	private String birdName;
	private double birdHeight;
	private boolean isBirdBig;
	public String getBirdName() 
	{
		return birdName;
	}
	public void setBirdName(String birdName) 
	{
		this.birdName = birdName;
	}
	public double getBirdHeight() 
	{
		return birdHeight;
	}
	public void setBirdHeight(double birdHeight) 
	{
		this.birdHeight = birdHeight;
	}
	public boolean getIsBirdBig() 
	{
		return isBirdBig;
	}
	public void setIsBirdBig(boolean isBirdBig) 
	{
		this.isBirdBig = isBirdBig;
	}
}
